// - Create an integer variable month with a value between 1 and 12.
// - Use a switch statement to print the season based on the month

void main() {
  int month = 1;
  switch (month) {
    case 1:
      print('Winter');
      break;
    case 2:
      print('Winter');
      break;
    case 3:
      print('Winter');
      break;

    case 4:
      print('the spring');
      break;
    case 5:
      print('the spring');
      break;
    case 6:
      print('the spring');

    case 7:
      print('the summer');
      break;
    case 8:
      print('the summer');
      break;
    case 9:
      print('the summer');

    case 10:
      print('the autumn');
      break;
    case 11:
      print('the autumn');
      break;
    case 12:
      print('the autumn');
      break;
      print('no season');

    default:
  }
}
